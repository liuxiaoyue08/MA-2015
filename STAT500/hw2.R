library(faraway)
data(uswages)
attach(uswages)
help(uswages)
summary(uswages)
uswages$exper[uswages$exper<0] <- NA
wage.reg<-lm(uswages$wage~uswages$educ+uswages$exper)
summary(wage.reg)
wage.res=residuals(wage.reg)
wage.largestno=which.max(wage.res)
wage.resmean=mean(wage.res)
wage.resmedian=median(wage.res)
summary(fitted(wage.reg))
wage.cor=cor(wage.res,fitted(wage.reg))
plot(fitted(wage.reg),wage.res)
logwage=log(uswages$wage)
logwage.reg<-lm(logwage~uswages$educ+uswages$exper)
summary(logwage.reg)
logwage.res=residuals(logwage.reg)
plot(fitted(logwage.reg),logwage.res)
plot(factor(uswages$exper),logwage)
abline(logwage.reg,col=2)