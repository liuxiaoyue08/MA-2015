library(faraway)
data(sat)
attach(sat)
help(sat)
h0<-lm(total~takers+ratio+salary,sat)
summary(h0)
h0a<-lm(total~takers+ratio+salary,sat)
anova(h0,h0a)
h0b<-lm(total~0,sat)
anova(h0,h0b)
confint(h0,"salary",level=0.95)
confint(h0,"salary",level=0.99)
install.packages("ellipse")
library(ellipse)
plot(ellipse(h0,c('ratio','salary')),type="l",xlim=c(-10,1))
points(0,0,pch=1)
points(h0$coef['ratio'],h0$coef['salary'],pch=18)
conf<-confint(h0)
abline(v=conf['ratio',],lty=2)
abline(h=conf['salary',],lty=2)
h1<-lm(total~expend+takers+ratio+salary,sat)
h1a<-lm(total~takers,sat)
anova(h1,h1a)